	<h2 class="c1 c8" id="h.f4lmag78ydlv"><span class="c6">What&rsquo;s New</span></h2>
	<p class="c1">
		<span class="c3">Simplified Zones</span><span class="c5"
			>: We felt that Portland had too many types of zones and many of these zones were overly
			restrictive. This latest update reduces the number of low-density residential zones by merging
			them into a more permissive low-density residential zone. It also merges the residential
			professional zone into a simpler and more permissive mixed-use zone. We would still like to
			see more simplification of the zoning code, but this change fully adopts one of our proposals
			and simplifies the code while adding more options for housing and businesses. We feel this is
			a major improvement.</span
		>
	</p>
	<p class="c1">
		<span class="c3">More Transit-Oriented Development</span><span class="c5"
			>: We felt that the new TOD zones were mapped in limited areas that resembled spot zoning.
			This latest update maps these areas onto priority nodes in a sensible way that does not favor
			specific properties and is in line with the comprehensive plan. However, the comprehensive
			plan is clear that development should be encouraged on both nodes and priority corridors and
			the corridors remain neglected. That said, these nodes will do a lot to foster transit-served
			neighborhood centers in our city. We feel this is a major improvement.</span
		>
	</p>
	<p class="c1">
		<span class="c3">More Mixed Use</span><span class="c5"
			>: We felt that the city did not have enough mixed use and the mixed use we did have were
			mapped to specific parcels. More mixed use has been mapped in places aligned with the
			comprehensive plan, like Brighton Avenue. This is also a step towards encouraging development
			on priority corridors. We still feel that the current level of allowed mixed use is
			insufficient, but this is a major improvement.</span
		>
	</p>
	<p class="c1">
		<span class="c3">Fewer Building </span><span class="c3">Length</span><span class="c3"
			>&nbsp;Limits</span
		><span class="c5"
			>: We felt that the new building length limits would make it difficult to build multifamily
			housing. The building length limits from the transit-oriented development zones were removed
			and the limit was increased very slightly (from 60 ft to 70 ft) in our multi-family
			residential zone. While we are very encouraged by the removal of this proposed limit from the
			transit-oriented development zones, we feel strongly that this building length limit in our
			only multi-family residential zone will make building housing to modern standards a challenge.
			This limit is not being relaxed by ReCode, ReCode is adding a new limit where it did not exist
			before. We don&rsquo;t want to see this limit added anywhere but for the transit-oriented
			development zone we feel this update is a major improvement.</span
		>
	</p>
	<p class="c1 c10"><span class="c5" /></p>
	<h2 class="c1 c8" id="h.sgf2yaf6r2fn"><span class="c6">Areas for improvement</span></h2>
	<p class="c1">
		<span class="c3">Further Simplify Zones</span><span class="c2"
			>: Though zones have been simplified, there are a few places where we feel they could be
			simplified further. The office zone now only covers the Unum office; it makes sense to zone
			this as mixed use for simplicity and potential housing in the far future. The densest
			off-peninsula residential zone could be merged into our on-peninsula, multi-family,
			residential zone to allow new denser more walkable neighborhoods to grow where it makes sense
			off-peninsula.</span
		>
	</p>
	<p class="c1">
		<span class="c3">Prioritize Corridors as well as Nodes</span><span class="c11 c2 c12"
			>: We are glad to see transit-oriented development in our priority nodes, however the
			comprehensive plan also specifies that corridors should be a focus. Though corridors might not
			be immediately adjacent to a major transit stop, residents can still comfortably walk to a
			stop, which &nbsp;will further drive transit ridership. This prioritization could look like
			either &nbsp;additional transit-oriented development zones or mixed-use zones added along
			these corridors.</span
		>
	</p>
	<p class="c1">
		<span class="c3">More Neighborhood Business</span><span class="c5"
			>: We are encouraged to see more mixed use mapped in ReCode. This may be sufficient for
			Portland&rsquo;s current need. However, we feel that allowing small-scale mixed use in more
			places will plant the seeds for new neighborhood centers to grow in the future.</span
		>
	</p>
	<p class="c1">
		<span class="c3">Remove Building </span><span class="c3">Length</span><span class="c3"
			>&nbsp;Limits</span
		><span class="c5"
			>: Despite a small increase in the proposed limit, the multi-family residential zone building
			length limit will make building multifamily housing a challenge with our current building
			codes. We would like to see this limit not added under ReCode and if it is added we would like
			it to be significantly higher.</span
		>
	</p>
	<p class="c1">
		<span class="c3">Multifamily Housing around Neighborhood Centers</span><span class="c2"
			>&nbsp;</span
		><span class="c11 c2 c12"
			>:We are encouraged to see our planning staff dedicated to making new neighborhood centers
			with mixed-use and transit-oriented development zones. However, the slightly denser
			residential zone that usually surrounds these centers does not allow multifamily housing.
			Surrounding our neighborhood centers with multifamily housing creates a more natural gradient
			between the denser neighborhood centers and the less dense residential zones. This will bring
			more residents into the neighborhood centers and make shops, restaurants, and transit stops
			viable.</span
		>
	</p>
	<p class="c1">
		<span class="c3">Gentle Density in Residential Zones</span><span class="c2">: </span><span
			class="c11 c2 c12"
			>LD2003 has opened up the potential for up to four units in most of the city, but we feel
			there are still some changes needed to help realize that potential. Decreasing lot sizes,
			setbacks, and density limits will help build a more walkable style of housing and make
			building accessory dwelling units (ADUs) possible on more lots.</span
		>
	</p>
	<p class="c1">
		<span class="c3">More Projects subject to Minor Site Plan Review</span><span class="c5"
			>: The ReCode proposal moves the threshold for a major site plan from 3 units to 5 units.
			While this is a step in the right direction, it is a very small one. A 6-unit building has
			more in common with a single-family home than a 300-unit apartment building, but the former
			apartments will go through the same process as the latter. We think basing the threshold on
			square feet instead of the number of units will make it easier and faster to build smaller
			projects.</span
		>
	</p>
	<p class="c1">
		<span class="c3">Allow for Higher Quality Stories</span><span class="c5"
			>: Modern building practices require more insulation and internal air systems for homes that
			are more environmentally friendly and comfortable. This was not the case when heights were
			calculated in our land use code. By adding a small amount of height to each story, we could
			make it easier to add these things with almost no noticeable effect from the outside. It
			seemed that there was some interest about this from the Planning Board and we would like to
			see it in the final draft of ReCode.</span
		>
	</p>
	<p class="c1">
		<span class="c3">Measure Height More Consistently</span><span class="c5"
			>: The proposed ReCode changes add much-needed clarification to the way heights are currently
			measured, but we feel there is opportunity for a new and improved methodology. The current
			method doesn&rsquo;t match the experience of someone on the street looking at the building,
			and doesn&rsquo;t align with where a builder is required to set the first floor, creating the
			potential for wasted space, lost units, and worse buildings. Details about how the lot is
			sloped can affect how much height a building is allowed at the front of their lot. By
			measuring based on the sidewalk in front of the building, we can create more consistency and
			less confusion when calculating measurements.</span
		>
	</p>
	<p class="c1">
		<span class="c3">Explore ReZoning Industrial Land</span><span class="c5"
			>: Some of Portland&rsquo;s industrial land is important to retain, but we feel that there are
			some opportunities to reconsider this use, especially on the peninsula. This land could be
			freed up for housing close to downtown where housing makes sense.
		</span>
	</p>

	<h3>
		Amendment 1: Do not add the limit preventing lots that were not vacant on July 1<sup>st</sup>,
		2023 from ever qualifying as vacant for LD2003
	</h3>
	<p>
		The current planning staff proposal adds a restriction to prevent lots that had a home on them
		on July 1st 2023 from ever being considered vacant for the purposes of LD2003. This restriction
		prevents these lots from qualifying for up to four units in the same structure. It limits the
		number of buildable lots that will qualify from about 28% of our city’s land to just about half
		a percent. The lots that would qualify are lots that could be built on today but aren’t. Usually
		this is because there is something about the lot that makes it unsuitable for housing. It also
		requires people to do historical research just to know what they can or can’t build. This
		restriction is overly complicated and stands in the way of not just a high quantity of housing,
		but housing in the places we need it. It should not be added to our land use code.
	</p>


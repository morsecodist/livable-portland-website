	<h2 class="text-secondary">Urbanist Coalition of Portland Amendments</h2>
	<p>
		We feel it is critical that we update our land use code in a way that honors the spirit of
		LD2003, however we acknowledge that our city has limited time to comply. This is why we have put
		together just four short amendments to the current planning staff proposal. Each of these
		amendments is brief and leaves code simpler than it would be if we passed the planning staff
		proposal as written. Three out of four of them are simply not adding limits to the LD2003
		allowances. The fourth just replaces the new, more restrictive rules for these units with our
		existing accessory dwelling unit (ADU) rules. These amendments are not complicated and we can
		make them without missing the January 2024 deadline. With these minor changes we will unlock the
		potential for not only more housing opportunities but the housing opportunities that are more
		affordable, more sustainable, and where we need them.
	</p>
	<p>
		Portland Planning Staff's proposal is in the form of <b style="color: #871723">red</b> and

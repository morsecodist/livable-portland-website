	<h3>
		Amendment 4: Give the additional units allowed by LD2003 the same benefits as our current ADUs
	</h3>
	<p>
		Our current accessory dwelling units (ADUs) are exempt from parking requirements, lot coverage
		maximums, and density limits. Meanwhile, the new units permitted by LD2003 have a different set
		of rules that only exempts them from density limits if they are in the same structure. While the
		units allowed by LD2003 are not the same as ADUs, the rules we have in place for ADUs work and
		there is no reason we can’t apply them to these new units as well.
	</p>


<h1 class="text-primary">Portland Leviathan</h1>
<p>Conference on the Past, Present, & Future of Interstate 295</p>

<img src="/leviathan.png" alt="295 Leviathan" />

<div class="text-start">
	<p>
		In the 20<sup>th</sup> century, cities across America weres carred by urban highways â€“ and Portland
		Maine is no exception. Here, I-295 was routed through underserved neighborhoods and communities of
		color permanently altering the city's landscape. During this time, the city also experienced the
		destruction of the iconic Union Station, the demolition of a thriving housing corporation, and the
		eradication of Maine's largest African American community at the time.
	</p>

	<p>
		Today, Portlanders are working to undo the damage and restore our city. Many citizen campaigns
		have centered around reckoning with the negative legacy of I-295, by advocating for the
		restoration of a walkable human scale city and the corollary benefits of environmental justice,
		economic development, vibrancy and beauty.
	</p>

	<p>
		Our efforts have been largely supported by the city government, which is seeking two
		<b>Reconnecting Communities</b> grants from the U.S. Department of Transportation to repair the neighborhood
		fabric at Franklin Street and at exit 5. However,as advocates work away along the margins, the root
		problem, I-295 itself, is often regarded as eternal and immutable.
	</p>

	<p class="lead pt-2">
		Portland Leviathan 2024 will look beyond isolated projects to envision a bolder, better future
		for the I-295 corridor, and our city as a whole.
	</p>

	<p class="fst-italic">
		Schedule, presenters, and partners are provisional and subject to change.
	</p>

	<h2 class="text-primary">Program</h2>

	<p>
		<b class="text-uppercase">Portland Leviathan 2024</b> will feature talks and discussions considering
		the broad context of urban highways in America, I-295 in particular, the effects on neighborhoods
		and communities, an overview of Street Light and other data collection methods, current projects,
		and plans for the future. There will also be a day time tour of the highway from bridge to bridge,
		and reception to follow. The conference will take place in Spring 2024, exact date to be determined,
		at the Jewish Community Alliance.
	</p>

